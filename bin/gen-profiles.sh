#!/usr/bin/env bash
#
# gen-profiles.sh, expose each agent as a Hermes *profile* so it shows up in the
# Hermes desktop app (and `hermes dashboard`, and `hermes profile list`).
#
# WHY PROFILES: the desktop app has no concept of this repo's shell-wrapper agents.
# What it DOES discover is profiles, it lists them via the gateway's /api/profiles
# endpoint, which is a plain directory scan of  ~/.hermes/profiles/<name>/  (verified:
# hermes_cli/profiles.py:list_profiles). A profile is just three files:
#   config.yaml, pins the model + the project's minimal-context settings
#   SOUL.md, the agent's system prompt (the desktop injects this as the persona)
#   .env, symlink to ~/.hermes/.env so the LiteLLM key is available (never copied)
#
# So "exposing an agent in the desktop" = materializing it as a profile here. This script
# generates one profile per agent from its agent.yaml (model alias) + SOUL.md (persona),
# reusing hermes-home/config.yaml as the base so the desktop chat behaves like the agent
# (same model, tools off, no reasoning block). Each generated profile is tagged with a
# marker file so --remove only ever deletes profiles WE created.
#
# Usage:
#   bin/gen-profiles.sh                 # generate/refresh profiles for every agent
#   bin/gen-profiles.sh gtm-planner …   # only the named agents
#   bin/gen-profiles.sh --prefix hermes-  # namespace profile names (hermes-gtm-planner)
#   bin/gen-profiles.sh --remove        # delete the profiles this script generated
#   bin/gen-profiles.sh --list          # show what would be generated, do nothing
#
# After running:  hermes desktop   (or `hermes dashboard`) → each agent is a chat persona.
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
HERMES_HOME_DEFAULT="${HERMES_AGENTS_PROFILE_HOME:-$HOME/.hermes}"
PROFILES_ROOT="$HERMES_HOME_DEFAULT/profiles"
BASE_CONFIG="$REPO_ROOT/hermes-home/config.yaml"
ENV_SRC="$HERMES_HOME_DEFAULT/.env"
MARKER=".hermes-agents-generated"

PREFIX=""
MODE="generate"
ARGS=()
while [ "$#" -gt 0 ]; do
  case "$1" in
    --prefix) PREFIX="$2"; shift 2 ;;
    --remove) MODE="remove"; shift ;;
    --list)   MODE="list"; shift ;;
    -h|--help) sed -n '2,30p' "$0"; exit 0 ;;
    *) ARGS+=("$1"); shift ;;
  esac
done

[ -f "$BASE_CONFIG" ] || { echo "gen-profiles: missing $BASE_CONFIG" >&2; exit 1; }

# yget <file> <key> [default], flat-YAML scalar reader (same convention as hermes-run.sh).
yget() {
  local file="$1" key="$2" def="${3-}" line val
  line="$(grep -E "^${key}:" "$file" | head -1 || true)"
  [ -n "$line" ] || { printf '%s' "$def"; return; }
  val="$(printf '%s' "$line" | sed -E "s/^${key}:[[:space:]]*//; s/[[:space:]]+#.*$//; s/^\"(.*)\"$/\1/; s/^'(.*)'$/\1/")"
  printf '%s' "$val"
}

# Discover agents: any dir holding agent.yaml + SOUL.md, one level under agents/
# OR one level under the gitignored agents/private/ overlay. Overlay agents are
# emitted as "private/<name>" (so the generate loop resolves the right source dir);
# the profile they materialize to uses the basename. In a public-only checkout
# agents/private/ holds only a README, so the second glob yields nothing.
discover_agents() {
  local d
  for d in "$REPO_ROOT"/agents/*/ "$REPO_ROOT"/agents/private/*/; do
    [ -f "$d/agent.yaml" ] && [ -f "$d/SOUL.md" ] || continue
    case "$d" in
      "$REPO_ROOT"/agents/private/*) printf 'private/%s\n' "$(basename "$d")" ;;
      *) basename "$d" ;;
    esac
  done
}

if [ "${#ARGS[@]}" -gt 0 ]; then AGENTS=("${ARGS[@]}"); else
  AGENTS=(); while IFS= read -r a; do AGENTS+=("$a"); done < <(discover_agents)
fi

if [ "$MODE" = "remove" ]; then
  for agent in "${AGENTS[@]}"; do
    name="$PREFIX$(basename "$agent")"; dir="$PROFILES_ROOT/$name"
    if [ -f "$dir/$MARKER" ]; then rm -rf "$dir"; echo "removed profile: $name"
    elif [ -d "$dir" ]; then echo "skip (not generated by us): $name" >&2; fi
  done
  exit 0
fi

mkdir -p "$PROFILES_ROOT"
for agent in "${AGENTS[@]}"; do
  src="$REPO_ROOT/agents/$agent"
  [ -f "$src/agent.yaml" ] && [ -f "$src/SOUL.md" ] || { echo "skip $agent: not an agent dir" >&2; continue; }
  alias="$(yget "$src/agent.yaml" alias)"
  maxturns="$(yget "$src/agent.yaml" max_turns 1)"
  name="$PREFIX$(basename "$agent")"
  # Description: first non-empty, non-heading line of the agent README (kanban/desktop hint).
  desc=""
  [ -f "$src/README.md" ] && desc="$(grep -vE '^\s*#|^\s*$' "$src/README.md" | head -1 | cut -c1-160 || true)"

  if [ "$MODE" = "list" ]; then
    printf '%-26s model=%-10s max_turns=%s\n' "$name" "$alias" "$maxturns"
    continue
  fi

  dir="$PROFILES_ROOT/$name"
  mkdir -p "$dir"
  # config.yaml: base minimal-context config, with the model + turn budget pinned per agent.
  sed -E "s/^  default: .*/  default: $alias/; s/^  max_turns: .*/  max_turns: $maxturns/" \
    "$BASE_CONFIG" > "$dir/config.yaml"
  # SOUL.md: the agent's system prompt becomes the desktop chat persona.
  cp -f "$src/SOUL.md" "$dir/SOUL.md"
  # .env: symlink the real key file (gitignored, never copied into the repo or profile).
  [ -f "$ENV_SRC" ] && ln -sf "$ENV_SRC" "$dir/.env"
  # Marker so --remove is safe.
  printf 'Generated by bin/gen-profiles.sh from agent "%s". Safe to delete.\n' "$agent" > "$dir/$MARKER"

  # Best-effort kanban/desktop description (non-fatal if the CLI shape differs).
  [ -n "$desc" ] && hermes profile describe "$name" "$desc" >/dev/null 2>&1 || true

  echo "profile ready: $name  (model=$alias)"
done

if [ "$MODE" = "generate" ]; then
  echo
  echo "Done. Verify:   hermes profile list"
  echo "Open desktop:   hermes desktop      (or: hermes dashboard)"
fi
